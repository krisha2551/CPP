// Relational Operators

int main() {
  
    int a = 10;
    int b = 6;

    // Greater than
    cout << "a > b = " << (a > b) << endl;
  
    // Less than
    cout << "a < b = " << (a < b) << endl;
  
    // Equal to
    cout << "a == b = " << (a == b) << endl;
  
    // Not equal to
    cout << "a != b = " << (a != b);

    return 0;
}